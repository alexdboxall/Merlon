static uint32_t ApplyBayerAdjustment(uint32_t colour, int change) {
    int r = (colour >> 16) & 0xFF;
    int g = (colour >> 8) & 0xFF;
    int b = colour & 0xFF;

    r += change;
    g += change;
    b += change;

    if (r < 0) r = 0;
    if (r > 255) r = 255;
    if (g < 0) g = 0;
    if (g > 255) g = 255;
    if (b < 0) b = 0;
    if (b > 255) b = 255;

    return (r << 16) | (g << 8) | b;
}

uint32_t GetBayerAdjustedColour8(int x, int y, uint32_t colour) {
    int change = (65 * (bayer_matrix_8[y % 8][x % 8] - 32)) / 64;
    return ApplyBayerAdjustment(colour, change);
}

uint32_t GetBayerAdjustedColour16(int x, int y, uint32_t colour) {
    int change = (65 * (bayer_matrix_16[y % 16][x % 16] - 128)) / 256;
    return ApplyBayerAdjustment(colour, change);
}